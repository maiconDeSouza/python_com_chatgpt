"""
2. **Operações Matemáticas**
- Crie um programa que leia dois números e exiba a soma, subtração, multiplicação e divisão deles.
"""

print("Digite o primeiro número")
n_1 = float(input())

print("Digite o segundo número")
n_2 = float(input())

print(f"A soma de {n_1} com {n_2} é igual a {n_1 + n_2}")
print(f"A subtração de {n_1} com {n_2} é igual a {n_1 - n_2}")
print(f"A multiplicação de {n_1} por {n_2} é igual a {n_1 * n_2}")
print(f"A divisão de {n_1} por {n_2} é igual a {n_1 / n_2}")
